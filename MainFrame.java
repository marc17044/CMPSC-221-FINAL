    public void rebuildSemesterComboBoxes() {
        currentSemesterLabel.setText(currentSemester);
        
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.isEmpty()) {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
        currentSemesterComboBox.setSelectedItem(currentSemester);
        
        ArrayList<String> courseList = CourseQueries.getAllCourseCodes();
        addClassCourseCodeComboBox.setModel(new javax.swing.DefaultComboBoxModel(courseList.toArray()));
        
        ArrayList<String> classList = ClassQueries.getAllCourseCodes(currentSemester);
        scheduleClassComboBox.removeAllItems();
        scheduleClassComboBox.setModel(new javax.swing.DefaultComboBoxModel(classList.toArray()));
        
        ClassDropClassComboBox.removeAllItems();
        ClassDropClassComboBox.setModel(new javax.swing.DefaultComboBoxModel(classList.toArray()));
        
        DisplayClassComboBox.removeAllItems();
        DisplayClassComboBox.setModel(new javax.swing.DefaultComboBoxModel(classList.toArray()));
        
        AdminDropClassComboBox.removeAllItems();
        AdminDropClassComboBox.setModel(new javax.swing.DefaultComboBoxModel(classList.toArray()));
        
        ArrayList<StudentEntry> studentEntryList = StudentQueries.getAllStudents();
        ArrayList<String> studentList = new ArrayList<>();;
        
        for (StudentEntry entry: studentEntryList){
            studentList.add(entry.getLastName()+", "+entry.getFirstName());
        }
        scheduleStudentComboBox.removeAllItems();
        scheduleStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentList.toArray()));
        
        
        
        
        StudentDropClassComboBox.removeAllItems();
        StudentDropClassComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentList.toArray()));
        
        selectStudentDisplayComboBox.removeAllItems();
        selectStudentDisplayComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentList.toArray()));
        
        studentList.clear();
        for (StudentEntry entry: studentEntryList){
            
            studentList.add(entry.getLastName()+", "+entry.getFirstName()+" "+entry.getStudentID());
        }
        DropStudentComboBox.removeAllItems();
        DropStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentList.toArray()));
        
    }

