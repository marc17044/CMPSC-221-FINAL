    private void scheduleClassSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleClassSubmitButtonActionPerformed
        String name = (String) scheduleStudentComboBox.getSelectedItem();
        String status = "scheduled";
        String courseCode = (String) scheduleClassComboBox.getSelectedItem();
        
        if (ScheduleQueries.getScheduledStudentCount(currentSemester, courseCode) >= ClassQueries.getClassSeats(currentSemester, courseCode)){
            
            status = "waitlisted";
        }
        
        ScheduleEntry entry = new ScheduleEntry(currentSemester,courseCode,StudentQueries.getStudentID(name),status);
        
        ScheduleQueries.addScheduleEntry(entry);
        scheduleClassStatusLabel.setText(name+" has been "+status+".");
        rebuildSemesterComboBoxes();
    }//GEN-LAST:event_scheduleClassSubmitButtonActionPerformed

