    private void DisplayClassListButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DisplayClassListButtonActionPerformed

        String courseCode = (String) DisplayClassComboBox.getSelectedItem();
        ArrayList<String> studentIDs = ScheduleQueries.getScheduledStudentByCourse(currentSemester, courseCode);
        
        DefaultTableModel model = (DefaultTableModel) ScheduledStudentsTable.getModel();
        model.setRowCount(0);
                
        for (String studentID : studentIDs) {
            StudentEntry entry = StudentQueries.getStudent(studentID);
            model.addRow(new Object[]{entry.getFirstName(),entry.getLastName(), entry.getStudentID()});
        }
        
        ArrayList<String> WaitlistedStudentIDs = ScheduleQueries.getWaitlistedStudentByCourse(currentSemester, courseCode);
        
        DefaultTableModel wModel = (DefaultTableModel) WaitlistedStudentsTable.getModel();
        wModel.setRowCount(0);
                
        for (String studentID : WaitlistedStudentIDs) {
            StudentEntry entry = StudentQueries.getStudent(studentID);
            wModel.addRow(new Object[]{entry.getFirstName(),entry.getLastName(), entry.getStudentID()});
        }
        
        
        
        

        rebuildSemesterComboBoxes();
    }//GEN-LAST:event_DisplayClassListButtonActionPerformed

    private void DropStudentSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropStudentSubmitButtonActionPerformed
        DropStudentTextArea.setText("");
        String text = (String) DropStudentComboBox.getSelectedItem();
        String studentId = text.substring(text.lastIndexOf(" ") + 1);
        DropStudentTextArea.append(text+" has benn dropped from the list of students\n");
        
        ArrayList<String> semesterList = SemesterQueries.getSemesterList();
        
        
        for (String semester:semesterList){
            DropStudentTextArea.append("\nFor Semester: "+semester+"\n");
            
            ArrayList<ScheduleEntry> schedules = ScheduleQueries.getScheduleByStudent(semester,studentId);
            for (ScheduleEntry schedule : schedules){
                if (schedule.getStatus().equals("scheduled")){
                    
                    DropStudentTextArea.append(text+" has been dropped from "+schedule.getCourseCode()+"\n");
                    
                    if (!ScheduleQueries.getWaitlistedStudentByCourse(semester, schedule.getCourseCode()).isEmpty()){
                        String newStudentId = ScheduleQueries.getWaitlistedStudentByCourse(semester, schedule.getCourseCode()).get(0);
                        String name = StudentQueries.getStudent(newStudentId).getFirstName()+" "+StudentQueries.getStudent(newStudentId).getLastName();
                        
                        
                        ScheduleQueries.setStatusToScheduled(newStudentId);
                        DropStudentTextArea.append(newStudentId+", "+name+" has been scheduled into "+schedule.getCourseCode()+"\n");
                    }
                    
                    
                    //check for waitlisted
                    
                }else{
                    DropStudentTextArea.append(text+" has been dropped from the waitlist for "+schedule.getCourseCode()+"\n");
                    //drop from waitlist
                    
                    
                }
                ScheduleQueries.dropSchedule(studentId,semester);
                
            }
        }
        StudentQueries.dropStudent(studentId);
        
        rebuildSemesterComboBoxes();
    }//GEN-LAST:event_DropStudentSubmitButtonActionPerformed

